namespace CollabApplication\Layout\Menu;

class MenuItem
{
    private $id;
    private $label;
    private $link;

    public function __construct($id, $label, $link = null)
    {
        $this->id = $id;
        $this->label = $label;
        $this->link = $link;
    }

    public function getId()
    {
        return $this->id;
    }

    public function setId($id)
    {
        $this->id = $id;
        return $this;
    }

    public function getLabel()
    {
        return $this->label;
    }

    public function setLabel($label)
    {
        $this->label = $label;
        return $this;
    }

    public function getLink()
    {
        return $this->link;
    }

    public function setLink($link)
    {
        $this->link = $link;
        return $this;
    }
}
